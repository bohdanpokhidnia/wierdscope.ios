import SwiftUI

extension Font {
    static func montserrat(size: CGFloat) -> Font {
        .custom("Montserrat", size: size)
    }
    
    static func IBMPlexSerif(size: CGFloat) -> Font {
        .custom("IBMPlexSerif-Regular", size: size)
    }
}
